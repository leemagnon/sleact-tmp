import useInput from '@hooks/useInput';
import React, { useState, useCallback } from 'react';
import { Link } from 'react-router-dom';
import { Header, Form, Label, Input, Button, LinkContainer, Error, Success } from './styles';
import axios from 'axios';

const SignUp = () => {
  const [email, onChangeEmail] = useInput('');
  const [nickname, onChangeNickname] = useInput('');
  const [password, , setPassword] = useInput('');
  const [passwordCheck, , setPasswordCheck] = useInput('');
  const [mismatchError, setMismatchError] = useState(false);
  const [signUpError, setSignUpError] = useState('');
  const [signUpSuccess, setSignUpSuccess] = useState(false);
  
  const onChangePassword = useCallback((e) => {
    setPassword(e.target.value);
    setMismatchError(e.target.value !== passwordCheck);
  }, [passwordCheck]);

  const onChangePasswordCheck = useCallback((e) => {
    setPasswordCheck(e.target.value);
    setMismatchError(e.target.value !== password);
  }, [password]);

  const onSubmit = useCallback((e) => {
    e.preventDefault();
    console.log(email, nickname, password, passwordCheck, mismatchError);
    
    if(!mismatchError && nickname) {
      console.log('서버로 회원가입하기');

      // 비동기 요청을 보내기 전에 이전 데이터가 남아있을 수 있으므로 사전에 초기화해주는 것이 좋다.
      setSignUpError('');
      setSignUpSuccess(false);

      axios.post('http://localhost:3095/api/users', { // <- localhost:3095가 3095에게 보냄. 'http://localhost:3095/api/users' 는 localhost:3090이 3095에게 보내는 것.
        email,
        nickname,
        password
      })
      .then((response) => {
        console.log(response);
        setSignUpSuccess(true);
      })
      .catch((error) => {
        console.log(error.response);
        setSignUpError(error.response.data);
      })
      .finally(() => {});

    }
  }, [email, nickname, password, passwordCheck, mismatchError]);

  return (
    <div id="container">
      <Header>Sleact</Header>
      <Form onSubmit={onSubmit}>
        <Label id="email-label">
          <span>이메일 주소</span>
          <div>
            <Input type="email" id="email" name="email" value={email} onChange={onChangeEmail} />
          </div>
        </Label>
        <Label id="nickname-label">
          <span>닉네임</span>
          <div>
            <Input type="text" id="nickname" name="nickname" value={nickname} onChange={onChangeNickname} />
          </div>
        </Label>
        <Label id="password-label">
          <span>비밀번호</span>
          <div>
            <Input type="password" id="password" name="password" value={password} onChange={onChangePassword} />
          </div>
        </Label>
        <Label id="password-check-label">
          <span>비밀번호 확인</span>
          <div>
            <Input
              type="password"
              id="password-check"
              name="password-check"
              value={passwordCheck}
              onChange={onChangePasswordCheck}
            />
          </div>
          {mismatchError && <Error>비밀번호가 일치하지 않습니다.</Error>}
          {!nickname && <Error>닉네임을 입력해주세요.</Error>}
          {signUpError && <Error>{signUpError}</Error>}
          {signUpSuccess && <Success>회원가입되었습니다! 로그인해주세요.</Success>}
        </Label>
        <Button type="submit">회원가입</Button>
      </Form>
      <LinkContainer>
        이미 회원이신가요?&nbsp;
        <Link to="/login">로그인 하러가기</Link> {/* <a href='...'></a> 는 화면이 새로고침 돼버림. */}
      </LinkContainer>
    </div>
  );
};

export default SignUp;
